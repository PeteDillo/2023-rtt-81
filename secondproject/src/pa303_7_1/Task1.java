package pa303_7_1;

public class Task1 {
	public static void main(String[] args) {
        int[] myArray = new int[3]; // Create an array of integers with a length of 3
        myArray[0] = 1; // Assign the value 1 to the index 0
        myArray[1] = 2; // Assign the value 2 to the index 1
        myArray[2] = 3; // Assign the value 3 to the index 2

        // Print out each array element
        for (int i = 0; i < myArray.length; i++) {
            System.out.println(myArray[i]);
        }

	}

}
